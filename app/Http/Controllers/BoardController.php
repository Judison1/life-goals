     */
    public function store(Request $request)
    {
        DB::beginTransaction();
        try {
            $board = Auth::user()->boards()->create($request->all());
            DB::commit();
            return redirect()->route('dashboard::boards.show', $board)->with('success', 'Quadro cadastrado com sucesso!');
